def pick_model_for_route(route: str, user_model: str, available_models: list[str] | None = None) -> str:
    """
    РђРІС‚Рѕ-РІС‹Р±РѕСЂ РјРѕРґРµР»Рё. Р•СЃР»Рё user_model РќР• РґРµС„РѕР»С‚ вЂ” СѓРІР°Р¶Р°РµРј РІС‹Р±РѕСЂ РїРѕР»СЊР·РѕРІР°С‚РµР»СЏ.
    Р•СЃР»Рё РґРµС„РѕР»С‚ вЂ” РїРѕРґР±РёСЂР°РµРј Р»СѓС‡С€СѓСЋ РґРѕСЃС‚СѓРїРЅСѓСЋ РјРѕРґРµР»СЊ РїРѕРґ route.
    РњР°РїРїРёРЅРі Р±РµСЂС‘С‚СЃСЏ РёР· РЅР°СЃС‚СЂРѕРµРє РїРѕР»СЊР·РѕРІР°С‚РµР»СЏ (SQLite).
    """
    if user_model != DEFAULT_MODEL:
        return user_model

    route_map = _get_route_map()
    candidates = route_map.get(route, route_map.get("chat", [DEFAULT_MODEL]))

    if available_models:
        available_set = set(available_models)
        for candidate in candidates:
            if candidate in available_set:
                return candidate

    return candidates[0] if candidates else user_model


